const notes = ['Note 1', 'Note 2', 'Note 3', 'Note4']

console.log(notes)


// How many item are in your array (length property) 
console.log(notes.length)
// Length -is a property, so we don't actually call it as a function


// grab an individual item from the list
console.log(notes[2])

// access item starting at the end of the list
console.log(notes[notes.length - 1])
// access item second to last item
console.log(notes[notes.length - 2])