//  ===  Equality Operator
let temp = 31 
let isFreezing = temp === 31 
console.log('( === ) Equality Operator:--> ' + isFreezing)


//  !==  Not Equal Operator
let temp1 = 31 
let isFreezing1 = temp !== 32 
console.log('( !== ) Not Equal Operator:--> ' + isFreezing1)


//  <  Less Then Operator
let temp2 = 31 
let isFreezing2 = temp < 32
console.log('( < ) Less Then Operator:--> ' + isFreezing2)


//  >  Greater Then Operator
let temp3 = 100 
let isFreezing3 = temp > 32 // Equality Operator
console.log('( > ) Greater Then Operator:--> ' + isFreezing3)


//  >=  Greater Then  OR Equal to Operator
let temp4 = 32 
let isFreezing4 = temp >= 32 // Equality Operator
console.log('( >= ) Greater Then  OR Equal to Operator:--> ' + isFreezing4)


